/**
 * Utility to convert emoji to OpenMoji SVG URL
 * OpenMoji uses lowercase hex codepoints in filenames
 * CDN: https://cdn.jsdelivr.net/npm/openmoji@latest/color/svg/
 * 
 * @param emoji - The emoji string (e.g., 'ğŸ ')
 * @returns The OpenMoji SVG URL
 */
export function getOpenMojiUrl(emoji: string): string {
  // Use Array.from to handle surrogate pairs correctly
  const codePoints: number[] = [];
  for (const char of emoji) {
    const codePoint = char.codePointAt(0);
    if (codePoint !== undefined) {
      codePoints.push(codePoint);
    }
  }
  
  // Convert to lowercase hex with proper padding
  // OpenMoji files use lowercase hex separated by '-' for sequences
  const codepoint = codePoints
    .map(cp => cp.toString(16).toLowerCase().padStart(4, '0'))
    .join('-');
  
  // Return OpenMoji CDN URL
  return `https://cdn.jsdelivr.net/npm/openmoji@latest/color/svg/${codepoint}.svg`;
}

